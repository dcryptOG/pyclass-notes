# NUMBERS: - store numerical info in 2 forms
# INTEGERS: int - whole numbers
# FLOATING POINT: float - numbers with a decimal

# STRINGS: str - ordered sequence of characters (IMMUTABLE)
# LISTS: list - ordered sequence of objects (MUTABLE) [10, "a", 2.4]
# TUPLES: tup - ordered sequence of objects (IMMUTABLE) SYNTAX (10,"a",2.4)

# SETS: set - unordered collection of unique objects SYNTAX {"a", "b"}
# DICTIONARY: dict - unordered KEY-VALUE pairs SYNTAX {"key1":"value1", "key2","value2"}
